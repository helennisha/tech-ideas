class cf1{
//Even or odd 
    public static void main(String args[]){
        System.out.print("Even numbers are");
       for(int i=1;i<=50;i++){
        if (i%2==0){                                                                                           
            System.out.print(   i+ " ");
        }
        
        }

        System.out.println("odd numbers are");
        for(int i=1;i<=50;i++){
            if (i%2!=0){                                                                                           
                System.out.print(   i+ " ");
            }
            
            }
       } 
    }
